public class e102 {

    public static void main(String[] args) {
        int summe = 0;
        for (int i = 1; i <= 10; i++) {
            summe += i;
        }
        System.out.println("Die Summe der ganzen Zahlen von 1 bis 10 ist: " + summe);
    }

}
